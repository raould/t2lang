export type ASTTag = (typeof AST_TAGS)[number];

export const AST_TAG_SET = new Set<ASTTag>(AST_TAGS);

export const assertAstTag = (tag: string, ctx?: { getText?: () => string }) => {
  if (!AST_TAG_SET.has(tag as ASTTag)) {
    const snippet = ctx && ctx.getText ? ctx.getText() : "<unknown>";
    throw new Error(`desugar produced non-core tag ${tag} at ${snippet}`);
  }
};
